RUSSIAN_CHARS  = set('??????????????????????????????????????????????????????????????????')
TAJIK_CHARS    = set('????????????????????????????????????????????????????????????????????????')
TAJIK_SPECIFIC = set('??????')


def detect_language(text: str) -> str:
    if not text:
        return 'en'

    text_chars = set(text)

    # Check for Tajik-specific characters first
    if text_chars & TAJIK_SPECIFIC:
        return 'tj'

    # Count Cyrillic characters
    cyrillic_count = sum(1 for c in text if c in RUSSIAN_CHARS)
    total_alpha    = sum(1 for c in text if c.isalpha())

    if total_alpha == 0:
        return 'en'

    cyrillic_ratio = cyrillic_count / total_alpha

    if cyrillic_ratio > 0.5:
        return 'ru'

    return 'en'


def get_language_name(code: str) -> str:
    mapping = {'en': 'English', 'ru': 'Russian', 'tj': 'Tajik'}
    return mapping.get(code, 'English')
